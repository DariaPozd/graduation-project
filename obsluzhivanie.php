						<b>Какие проблемы решает сервисное обслуживание кондиционеров? И что будет, если его не делать?</b><br><br>
						Во время работы наружный и внутренний блоки кондиционера пропускают через себя очень много воздуха. И, как следствие, накапливают довольно много пыли. Еcли загрязнён конденсатор наружного блока, он не может эффективно отводить тепло, появляющееся во время сжатия фреона, что вызывает повышение давления в системе и увеличивает нагрузку на компрессор. Что, в свою очередь, уменьшает его ресурс и приводит к серьёзной поломке. Не менее опасно загрязнение фильтров испарителя внутреннего блока. Пыль, осевшая на фильтрах, не позволяет эффективно отводить холод, вырабатывающийся при кипении фреона. В результате не закипевший фреон в жидкой фазе попадает на вход компрессора, что также может вызвать его поломку.<br>
						Кроме того, в процессе эксплуатации кондиционера может испаряться до 8% фреона в год. Регулярный сервис сплит-системы помогает выявить необходимость дозаправки или полной перезаправки (в зависимости от типа применяемого хладагента) и исключить ситуацию с отсутствием газа в компрессоре, которая может привести к серьёзной поломке и, как следствие, к дорогостоящему ремонту оборудования.<br>
